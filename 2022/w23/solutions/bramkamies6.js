module.exports = (a, b) => {
    let aPow = a >= 1_000_000 ? 1_000_000 :
        a >= 100_000 ? 100_000 :
            a >= 10_000 ? 10_000 :
                a >= 1_000 ? 1_000 :
                    a >= 100 ? 100 :
                        a >= 10 ? 10 : 1;
    let bPow = b >= 1_000_000 ? 1_000_000 :
        b >= 100_000 ? 100_000 :
            b >= 10_000 ? 10_000 :
                b >= 1_000 ? 1_000 :
                    b >= 100 ? 100 :
                        b >= 10 ? 10 : 1;
    const ans = a - b + (~(a / aPow) - ~(b / bPow)) * (aPow + bPow);
    return ans < 0 ? -ans : ans;
};